function [IGF,err] = IGF_LRsmooth_v1a_Func(IGF,INPUT)

Status2('busy','Gradient Fix',3);

err.flag = 0;
err.msg = '';

%---------------------------------------------
% Get Input
%---------------------------------------------
G0 = INPUT.G0;
%GQNT = INPUT.GQNT;
clear INPUT
nproj = length(G0(:,1,1));

%---------------------------------------------
% Smooth
%---------------------------------------------
G = zeros(size(G0));
for n = 1:nproj
    for d = 1:3
        G(n,:,d) = smooth(G0(n,:,d),IGF.smoothwin);
    end
end
   
%---------------------------------------------
% Return
%---------------------------------------------
IGF.G0fix = G;

Status2('done','',3);
