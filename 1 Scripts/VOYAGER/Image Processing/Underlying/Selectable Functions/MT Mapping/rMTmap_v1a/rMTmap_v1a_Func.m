function [MAP,err] = rMTmap_v1a_Func(MAP,INPUT)

Status2('busy','Generate Relative MT Map',2);
Status2('done','',3);

err.flag = 0;
err.msg = '';

%---------------------------------------------
% Get Input
%---------------------------------------------
IMG = INPUT.IMG;
Im = IMG.Im;
clear INPUT;

%---------------------------------------------
% Calculate
%---------------------------------------------
ImOut = squeeze(Im(:,:,:,2) - Im(:,:,:,1));

%---------------------------------------------
% Return
%---------------------------------------------
MAP.Im = ImOut;

Status2('done','',2);
Status2('done','',3);