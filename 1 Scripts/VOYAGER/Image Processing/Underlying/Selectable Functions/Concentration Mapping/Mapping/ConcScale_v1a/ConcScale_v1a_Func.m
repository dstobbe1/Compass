function [CMAP,err] = ConcScale_v1a_Func(CMAP,INPUT)

Status2('busy','Concentration Scale',2);
Status2('done','',3);

err.flag = 0;
err.msg = '';

%---------------------------------------------
% Get Input
%--------------------------------------------- 
Im = INPUT.Im;
clear INPUT;

%---------------------------------------------
% Scale
%--------------------------------------------- 
Im = CMAP.assign*(abs(Im)/CMAP.current);

%---------------------------------------------
% Return
%--------------------------------------------- 
CMAP.Im = Im;

Status2('done','',2);
Status2('done','',3);

